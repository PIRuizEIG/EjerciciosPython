# Intercambiar valores

# Guardamos los valores
a = input("Introduzca valor 1: ")
b = input("Introduzca valor 2: ")

# Mostramos el valor inicial
print("Inicio: Valor1", a, "Valor2", b)

# Intercambiamos
print("Intercambiar")
c = a
a = b
b = c

# Mostramos el valor cambiado
print("Fin: Valor1", a, "Valor2", b)